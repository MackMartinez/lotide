const findKey = (object, callback) => {
  const keyObj = Object.keys(object);
  // console.log(object[keyObj[1]]["stars"]);
  for (let x = 0; x < keyObj.length; x++) {
    // console.log(object[keyObj[x]]["stars"]);
    if (callback(object[keyObj[x]]) === true) {
      return keyObj[x];
    }
  }
};

